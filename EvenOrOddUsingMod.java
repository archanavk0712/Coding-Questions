package DailyDose;

public class EvenOrOddUsingMod 
{
	public static void main(String[] args)
	{
		int number = 5;
		
		// Check if the number is even or odd
	    if (number % 2 == 0) 
	    {
	    	System.out.println(number + " is even.");
	    }
	    else
	    {
	    	System.out.println(number + " is odd.");
	    }
	}
}
